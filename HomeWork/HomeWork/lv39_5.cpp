#include <iostream>

using namespace std;

int arr[6][2] = { {1,6 }, {3,8}, {8,9},  {2,4},  {4,6},  {7,9} };
int via[6] = {};
int max2 = 0;
int cnt = 0;


// 1번함수
////////////////////////////////////////////
void BBQ_1(int _lv, int _time)
{
	if (0 >= _time - (arr[_lv][1] - arr[_lv][0]))
	{
		if (cnt > max2)
			max2 = cnt;

		return;
	}

	for (size_t i = 0; i < 6; i++)
	{
		if (via[i] == 0)
		{
			++cnt;
			_time = _time - (arr[_lv][1] - arr[_lv][0]);
			via[i] = 1;
			BBQ_1(i, _time);
			_time = _time + (arr[_lv][1] - arr[_lv][0]);
			--cnt;
			via[i] = 0;

		}
	}
}
